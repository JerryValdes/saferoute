function latLngDistKm(a, b) {
  const R = 111.32;
  const dy = (a.lat - b.lat) * R;
  const dx = (a.lng - b.lng) * R * Math.cos((a.lat + b.lat) / 2 * Math.PI / 180);
  return Math.sqrt(dx * dx + dy * dy);
}

// Returns approximate area of a GeoJSON Polygon/MultiPolygon in km²
function estimateZoneAreaKm2(geometry) {
  if (!geometry) return 1;
  let ring;
  if (geometry.type === 'Polygon') {
    ring = geometry.coordinates[0];
  } else if (geometry.type === 'MultiPolygon') {
    // Use the largest outer ring
    ring = geometry.coordinates
      .map(p => p[0])
      .sort((a, b) => b.length - a.length)[0];
  }
  if (!ring || ring.length < 3) return 1;
  const lat0 = ring.reduce((s, c) => s + c[1], 0) / ring.length;
  const cosLat = Math.cos(lat0 * Math.PI / 180);
  const kmLat = 110.574, kmLng = 111.320 * cosLat;
  let area = 0;
  for (let i = 0, j = ring.length - 1; i < ring.length; j = i++) {
    area += (ring[j][0] * kmLng + ring[i][0] * kmLng) *
            (ring[j][1] * kmLat - ring[i][1] * kmLat);
  }
  return Math.max(Math.abs(area / 2), 0.01);
}

