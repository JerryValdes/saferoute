function buildCensusTractZones(features, acsMap) {
  features.forEach(feature => {
    const props = feature.properties || {};
    const geoid = props.GEOID;
    if (!geoid || !feature.geometry) return;
    const tractKey = `tract:${geoid}`;
    if (polyLayers[tractKey]) return;
    const geoFeature = { type: 'Feature', geometry: feature.geometry };
    const bounds = L.geoJSON(geoFeature).getBounds();
    const center = bounds.getCenter();
    if (isCoveredByCity(center.lat, center.lng)) return;
    const acs = acsMap[geoid] || {};
    const population = acs.population ?? -1;
    const income = acs.income ?? -1;
    const povertyRate = acs.povertyRate ?? 0.12;
    const unemploymentRate = acs.unemploymentRate ?? -1;
    const vacancyRate = acs.vacancyRate ?? -1;
    const crimeData = getCountyCrime(geoid.slice(0, 5));
    const { score, level, hasCrime } = computeTractScore(acs, crimeData);
    const name = props.NAME || `Census Tract ${parseInt(geoid.slice(5), 10) / 100}`;
    const st = STYLES[level];
    const geoLayer = L.geoJSON(geoFeature, { style: () => ({ ...st, weight: 0.5 }) }).addTo(censusLayerGroup);
    polyLayers[tractKey] = geoLayer;

    const crimeDesc = hasCrime
      ? `Census tract in a county with ${crimeData.violentPer1k.toFixed(1)} violent crimes per 1,000 residents (County Health Rankings 2022/FBI UCR).`
      : income > 0
        ? `Census tract with a median household income of $${income.toLocaleString()} and a poverty rate of ${(povertyRate * 100).toFixed(1)}%.`
        : `Census tract with a poverty rate of ${(povertyRate * 100).toFixed(1)}%.`;

    const zone = {
      name,
      geoid,
      acsScore: score,
      cityId: `census-${geoid.slice(0, 5)}`,
      cityLabel: `Census Tract · ${STATE_FIPS[geoid.slice(0, 2)] || 'US'}`,
      dataSource: hasCrime ? 'CHR/FBI UCR 2022 + ACS 2022' : 'ACS 5-Year 2022',
      level, score,
      desc: crimeDesc,
      tips: [
        ...(hasCrime ? [
          { icon: '🚔', text: `County violent crime: ${crimeData.violentPer1k.toFixed(1)}/1,000 residents (CHR 2022/FBI UCR)` },
        ] : []),
        ...(population > 0 ? [{ icon: '👥', text: `Est. population: ${population.toLocaleString()} (ACS 2022)` }] : []),
        ...(income > 0 ? [{ icon: '💰', text: `Median income: $${income.toLocaleString()}` }] : []),
        { icon: '📊', text: `Poverty rate: ${(povertyRate * 100).toFixed(1)}%` },
        ...(unemploymentRate >= 0 ? [{ icon: '💼', text: `Unemployment: ${(unemploymentRate * 100).toFixed(1)}%` }] : []),
        ...(vacancyRate >= 0 ? [{ icon: '🏠', text: `Housing vacancy: ${(vacancyRate * 100).toFixed(1)}%` }] : []),
        { icon: 'ℹ️', text: `GEOID: ${geoid} · ACS 2022` },
      ],
      crimeScore: null, qolScore: null, amenityScore: null, crimeCount: null,
      geometry: feature.geometry,
      _center: center,
      _area: estimateZoneAreaKm2(feature.geometry),
    };
    ZONES.push(zone);
    geoLayer.on('mouseover', () => geoLayer.setStyle({ fillOpacity: HOVER_OPACITY[level], weight: 1.5 }));
    geoLayer.on('mouseout',  () => geoLayer.setStyle({ ...st, weight: 0.5 }));
    geoLayer.on('click', e => { L.DomEvent.stopPropagation(e); showZone(zone); });
  });
}

