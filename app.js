function crimeWeight(type) {
  if (!type) return 0;
  const t = String(type).toUpperCase().replace(/-/g, ' ');
  for (const { match, weight } of CRIME_WEIGHTS) {
    if (t.includes(match)) return weight;
  }
  return 0;
}

function getCrimeType(crime, typeFields) {
  for (const f of typeFields) { if (crime[f]) return crime[f]; }
  return null;
}

// ── GeoJSON-aware point-in-polygon (Polygon + MultiPolygon) ─────────────────
function pointInRing(testLat, testLng, ring) {
  let inside = false;
  const n = ring.length;
  for (let i = 0, j = n - 1; i < n; j = i++) {
    const [lng1, lat1] = ring[i];
    const [lng2, lat2] = ring[j];
    if ((lat1 > testLat) !== (lat2 > testLat)) {
      const lngCross = (lng2 - lng1) * (testLat - lat1) / (lat2 - lat1) + lng1;
      if (testLng < lngCross) inside = !inside;
    }
  }
  return inside;
}

function pointInGeoJSON(testLat, testLng, geometry) {
  const polys = geometry.type === 'Polygon'
    ? [geometry.coordinates]
    : geometry.coordinates; // MultiPolygon
  for (const polygon of polys) {
    if (pointInRing(testLat, testLng, polygon[0])) return true;
  }
  return false;
}

