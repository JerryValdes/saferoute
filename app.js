// ── IndexedDB cache (falls back to localStorage on error) ────────────────────
let _idb = null;

function _openIdb() {
  if (_idb) return Promise.resolve(_idb);
  return new Promise((resolve, reject) => {
    const req = indexedDB.open('saferoute_cache', 1);
    req.onupgradeneeded = e => {
      const db = e.target.result;
      if (!db.objectStoreNames.contains('cache'))
        db.createObjectStore('cache', { keyPath: 'k' });
    };
    req.onsuccess = e => { _idb = e.target.result; resolve(_idb); };
    req.onerror   = () => reject(req.error);
  });
}

async function getCached(key, ttl) {
  try {
    const db  = await _openIdb();
    const row = await new Promise((res, rej) => {
      const req = db.transaction('cache', 'readonly').objectStore('cache').get(key);
      req.onsuccess = () => res(req.result);
      req.onerror   = () => rej(req.error);
    });
    if (!row || Date.now() - row.ts >= ttl) return null;
    return row.data;
  } catch {
    try {
      const raw = localStorage.getItem(key);
      if (!raw) return null;
      const { data, ts } = JSON.parse(raw);
      return Date.now() - ts < ttl ? data : null;
    } catch { return null; }
  }
}

async function setCached(key, data) {
  try {
    const db = await _openIdb();
    await new Promise((res, rej) => {
      const tx = db.transaction('cache', 'readwrite');
      tx.objectStore('cache').put({ k: key, data, ts: Date.now() });
      tx.oncomplete = res;
      tx.onerror    = () => rej(tx.error);
    });
  } catch {
    try { localStorage.setItem(key, JSON.stringify({ data, ts: Date.now() })); } catch {}
  }
}

