function nearbyHtml(zone) {
  const center = zoneCenterApprox(zone);
  if (!center) return '';
  const RADIUS_KM = 5;
  const COLOR = { safe: 'var(--safe)', caution: 'var(--caution)', avoid: 'var(--avoid)' };
  const candidates = [];
  for (let i = 0; i < ZONES.length; i++) {
    const z = ZONES[i];
    if (z === zone || !z.score) continue;
    const c = zoneCenterApprox(z);
    if (!c) continue;
    const km = latLngDistKm(center, c);
    if (km <= RADIUS_KM) candidates.push({ z, km, i });
  }
  if (candidates.length === 0) return '';
  candidates.sort((a, b) => b.z.score - a.z.score);
  const rows = candidates.slice(0, 8).map(({ z, km, i }) => {
    const dist = fmtDistKm(km);
    const cityPart = z.cityLabel && !z.cityLabel.startsWith('Census Tract ·') ? `<span class="nearby-city">${z.cityLabel}</span>` : '';
    return `<div class="nearby-item" onclick="showZoneByIdx(${i})">` +
      `<span class="nearby-dot" style="background:${COLOR[z.level] || '#94a3b8'}"></span>` +
      `<span class="nearby-name">${z.name}${cityPart}</span>` +
      `<span class="nearby-meta">${z.score}/5<br><span style="font-weight:400">${dist}</span></span>` +
      `</div>`;
  }).join('');
  return `<div class="nearby-section"><div class="nearby-heading">Nearby Neighborhoods</div><div class="nearby-list">${rows}</div></div>`;
}

