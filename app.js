async function fetchImdBatch(codes, serviceUrl, codeField, scoreField, total) {
  const uncached = codes.filter(c => !(c in imdCache));
  // Batch in chunks of 200 to stay under URL limits
  for (let i = 0; i < uncached.length; i += 200) {
    const chunk = uncached.slice(i, i + 200);
    try {
      const url = new URL(serviceUrl);
      url.searchParams.set('where', `${codeField} IN (${chunk.map(c => `'${c}'`).join(',')})`);
      url.searchParams.set('outFields', `${codeField},${scoreField}`);
      url.searchParams.set('returnGeometry', 'false');
      url.searchParams.set('resultRecordCount', '2000');
      url.searchParams.set('f', 'json');
      const res = await fetch(url.toString());
      if (res.ok) {
        const data = await res.json();
        (data.features || []).forEach(f => {
          const code = f.attributes?.[codeField];
          const rank = f.attributes?.[scoreField];
          if (code && rank) imdCache[code] = { rank, total };
        });
      }
    } catch {}
  }
  const result = {};
  codes.forEach(c => { if (c in imdCache) result[c] = imdCache[c]; });
  return result;
}

function ukScore(rank, total) {
  if (!rank || rank <= 0) return { score: 3.0, level: 'caution' };
  const pct = rank / total;
  const raw = 1 + 4 * pct;
  const score = Math.round(Math.min(5, Math.max(1, raw)) * 10) / 10;
  return { score, level: scoreToLevel(score) };
}

