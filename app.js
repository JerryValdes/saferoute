// ── Crime data fetch (Socrata, OData, ArcGIS, CKAN) ────────────────────────────
// period=0 → last 365 days; period=1 → prior 365 days (365-730 days ago, for trend)
async function fetchCrimes(cityId, city, period = 0) {
  if (city.noApi) return null;
  if (city.noDateFilter && period > 0) return null;
  const key = `saferoute_crimes_${cityId}_v6_p${period}`;
  const cached = await getCached(key, CACHE_TTL);
  if (cached) return cached;

  const toDate = new Date(); toDate.setDate(toDate.getDate() - 365 * period);
  const fromDate = new Date(); fromDate.setDate(fromDate.getDate() - 365 * (period + 1));
  const fromStr = fromDate.toISOString().slice(0, 10);
  const toStr   = toDate.toISOString().slice(0, 10);

  const [n, w, s, e] = city.bbox;
  const latF = city.latField || 'latitude';
  const lngF = city.lngField || 'longitude';
  const apiType = city.apiType || 'socrata';

  let data;
  if (!city.crimeEndpoint) throw new Error('No crime endpoint configured');
  if (apiType === 'socrata') {
    const url = new URL(city.crimeEndpoint);
    url.searchParams.set('$limit', String(city.limit || 50000));
    // Only fetch the fields we actually use — reduces payload by ~90%
    const neededFields = [...new Set([
      city.dateField,
      ...city.typeFields,
      ...(city.bboxField ? [city.bboxField] : [latF, lngF]),
    ])];
    url.searchParams.set('$select', neededFields.join(','));
    if (city.noDateFilter) {
      url.searchParams.set('$order', `${city.dateField} DESC`);
    } else {
      let where = `${city.dateField} >= '${fromStr}T00:00:00' AND ${city.dateField} < '${toStr}T00:00:00'`;
      if (city.bboxField) {
        where += ` AND within_box(${city.bboxField}, ${n}, ${w}, ${s}, ${e})`;
      } else if (!city.noBbox) {
        where += ` AND ${latF} >= ${s} AND ${latF} <= ${n} AND ${lngF} >= ${w} AND ${lngF} <= ${e}`;
      }
      url.searchParams.set('$where', where);
    }
    const res = await fetch(url.toString());
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    data = await res.json();

  } else if (apiType === 'odata') {
    const url = new URL(city.crimeEndpoint);
    url.searchParams.set('$top', '50000');
    url.searchParams.set('$format', 'json');
    if (!city.noDateFilter) {
      url.searchParams.set('$filter',
        `${city.dateField} ge '${fromStr}T00:00:00Z' and ${city.dateField} lt '${toStr}T00:00:00Z' and ${latF} ge ${s} and ${latF} le ${n} and ${lngF} ge ${w} and ${lngF} le ${e}`);
    } else {
      url.searchParams.set('$orderby', `${city.dateField} desc`);
    }
    const res = await fetch(url.toString());
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    const json = await res.json();
    data = json.value || json;

  } else if (apiType === 'arcgis') {
    const url = new URL(city.crimeEndpoint);
    const arcWhere = city.noDateFilter ? '1=1'
      : `${city.dateField} >= DATE '${fromStr}' AND ${city.dateField} < DATE '${toStr}'`;
    url.searchParams.set('where', arcWhere);
    const outFieldsList = [...new Set([...city.typeFields, latF, lngF])].join(',');
    url.searchParams.set('outFields', outFieldsList);
    url.searchParams.set('resultRecordCount', String(city.limit || 50000));
    url.searchParams.set('orderByFields', `${city.dateField} DESC`);
    url.searchParams.set('outSR', '4326');
    url.searchParams.set('f', 'json');
    const res = await fetch(url.toString());
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    const json = await res.json();
    data = (json.features || []).map(f => {
      const attrs = f.attributes || {};
      // Fall back to geometry coordinates when lat/lng not in attributes (e.g. Portland MapServer)
      const lat = attrs[latF] != null ? parseFloat(attrs[latF]) : f.geometry?.y ?? null;
      const lng = attrs[lngF] != null ? parseFloat(attrs[lngF]) : f.geometry?.x ?? null;
      return { ...attrs, latitude: lat, longitude: lng };
    });

  } else if (apiType === 'police_uk') {
    // data.police.uk: query last N months at multiple center points (1-mile radius each)
    const luRes = await fetch('https://data.police.uk/api/crime-last-updated');
    const lastUpdated = (await luRes.json()).date; // e.g. "2026-03-01"
    const [luY, luM] = lastUpdated.split('-').map(Number);
    const months = [];
    for (let i = 0; i < (city.months || 3); i++) {
      const d = new Date(luY, luM - 1 - i);
      months.push(`${d.getFullYear()}-${String(d.getMonth()+1).padStart(2,'0')}`);
    }
    const pts = city.queryPoints || [city.center];
    const allCrimes = [], seen = new Set();
    await Promise.all(pts.flatMap(([plat, plng]) => months.map(async month => {
      try {
        const r = await fetch(`${city.crimeEndpoint}?lat=${plat}&lng=${plng}&date=${month}`);
        if (!r.ok) return;
        const crimes = await r.json();
        for (const crime of (Array.isArray(crimes) ? crimes : [])) {
          if (crime.id && seen.has(crime.id)) continue;
          if (crime.id) seen.add(crime.id);
          const loc = crime.location || {};
          allCrimes.push({
            [city.typeFields[0]]: crime.category,
            latitude: parseFloat(loc.latitude) || 0,
            longitude: parseFloat(loc.longitude) || 0,
            [city.dateField]: crime.month,
          });
        }
      } catch {}
    })));
    data = allCrimes;

  } else if (apiType === 'ckan') {
    const latF2 = city.latField || 'latitude';
    const lngF2 = city.lngField || 'longitude';
    const fields = [...new Set([city.dateField, ...city.typeFields, latF2, lngF2])].map(f => `"${f}"`).join(',');
    const dateFilter = city.noDateFilter ? '' : `AND "${city.dateField}" >= '${fromStr}' AND "${city.dateField}" < '${toStr}'`;
    const sql = `SELECT ${fields} FROM "${city.resourceId}" WHERE 1=1 ${dateFilter} LIMIT 50000`;
    const url = `${city.crimeEndpoint}?sql=${encodeURIComponent(sql)}`;
    const res = await fetch(url);
    if (!res.ok) throw new Error(`CKAN HTTP ${res.status}`);
    const json = await res.json();
    if (!json.success) throw new Error(`CKAN query failed`);
    data = (json.result?.records || []).map(r => ({
      ...r,
      [latF2]: parseFloat(r[latF2]),
      [lngF2]: parseFloat(r[lngF2]),
    }));
  }

  if (!Array.isArray(data) || data.length === 0) throw new Error('No crime data returned');
  await setCached(key, data);
  return data;
}

