async function loadCensusTractsForBounds(bounds) {
  let geoJSON;
  try {
    geoJSON = await fetchTractGeoJSON(bounds);
  } catch (e) {
    console.warn('SafeRoute: TIGERweb fetch failed:', e);
    return;
  }
  if (!geoJSON.features?.length) return;

  const newFeatures = geoJSON.features.filter(f => {
    const geoid = f.properties?.GEOID;
    if (!geoid || polyLayers[`tract:${geoid}`]) return false;
    return true;
  });
  if (!newFeatures.length) return;

  const drawable = newFeatures.filter(f => {
    const geoid = f.properties.GEOID;
    if (!(parseInt(f.properties.AREALAND) > 0)) {
      polyLayers[`tract:${geoid}`] = 'covered';
      return false;
    }
    return true;
  });
  if (!drawable.length) return;

  const byCounty = {};
  drawable.forEach(f => {
    const geoid = f.properties.GEOID;
    const stateCode  = f.properties.STATE  || geoid.slice(0, 2);
    const countyCode = f.properties.COUNTY || geoid.slice(2, 5);
    const key = `${stateCode}|${countyCode}`;
    if (!byCounty[key]) byCounty[key] = { stateCode, countyCode, features: [] };
    byCounty[key].features.push(f);
  });
  await Promise.all(Object.values(byCounty).map(async ({ stateCode, countyCode, features }) => {
    const acsMap = await fetchACSForCounty(stateCode, countyCode).catch(e => {
      console.warn(`SafeRoute: ACS failed for ${stateCode}${countyCode}:`, e);
      return {};
    });
    buildCensusTractZones(features, acsMap);
  }));
}

async function _doLoadCensusTracts() {
  if (map.getZoom() < 10) return;
  await loadCensusTractsForBounds(map.getBounds());

  // Upgrade tracts in Socrata-covered cities with real crime data
  const bounds2 = map.getBounds();
  for (const [cityKey, config] of Object.entries(SOCRATA_CITIES)) {
    if (socrataLoaded.has(cityKey)) continue;
    const [n, w, s, e] = config.bbox;
    if (!bounds2.intersects(L.latLngBounds([s, w], [n, e]))) continue;
    socrataLoaded.add(cityKey);
    applySocrataCrimeToTracts(cityKey, config);
  }
}

