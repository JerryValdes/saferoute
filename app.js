;(function () {
  let state = 'peek';
  let dragging = false;
  let startClientY = 0, startTranslateY = 0;
  let velY = 0, prevClientY = 0, prevT = 0;

  function el() { return document.getElementById('sidebar'); }

  function currentTranslateY() {
    const s = getComputedStyle(el()).transform;
    if (!s || s === 'none') return 0;
    const m = s.match(/matrix\([^,]+,[^,]+,[^,]+,[^,]+,[^,]+,\s*([^)]+)\)/);
    return m ? parseFloat(m[1]) : 0;
  }

  function snapTo(newState, animated) {
    state = newState;
    const sidebar = el();
    sidebar.style.transition = animated !== false
      ? 'transform .32s cubic-bezier(.4,0,.2,1)' : 'none';
    if (newState === 'full')      sidebar.style.transform = 'translateY(0)';
    else if (newState === 'half') sidebar.style.transform = 'translateY(42%)';
    else                          sidebar.style.transform = 'translateY(calc(100% - 80px))';
  }

  window.setSheetState = function (s, animated) {
    if (window.innerWidth <= 700) snapTo(s, animated);
  };

  function onHandleTouchStart(e) {
    if (window.innerWidth > 700) return;
    dragging = true;
    startClientY = e.touches[0].clientY;
    startTranslateY = currentTranslateY();
    velY = 0; prevClientY = startClientY; prevT = Date.now();
    el().style.transition = 'none';
  }

  function onTouchMove(e) {
    if (!dragging || window.innerWidth > 700) return;
    const cy = e.touches[0].clientY;
    const now = Date.now();
    const dt = now - prevT;
    if (dt > 0) velY = (cy - prevClientY) / dt;
    prevClientY = cy; prevT = now;
    const newY = Math.max(0, startTranslateY + (cy - startClientY));
    el().style.transform = `translateY(${newY}px)`;
  }

  function onTouchEnd() {
    if (!dragging || window.innerWidth > 700) return;
    dragging = false;
    const dy = prevClientY - startClientY;
    let next = state;
    if (velY > 0.6 || dy > 70) {
      next = state === 'full' ? 'half' : 'peek';
    } else if (velY < -0.6 || dy < -70) {
      next = state === 'peek' ? 'half' : 'full';
    }
    snapTo(next);
  }

  document.addEventListener('DOMContentLoaded', function () {
    const handle = document.getElementById('sheet-handle');
    if (!handle) return;
    handle.addEventListener('touchstart', onHandleTouchStart, { passive: true });
    window.addEventListener('touchmove', onTouchMove, { passive: true });
    window.addEventListener('touchend', onTouchEnd);
    handle.addEventListener('click', function () {
      if (window.innerWidth > 700) return;
      if (Math.abs(prevClientY - startClientY) > 10) return;
      snapTo(state === 'peek' ? 'half' : state === 'half' ? 'full' : 'peek');
    });
  });
})();

