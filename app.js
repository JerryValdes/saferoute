// ── 311 & amenity score computation ──────────────────────────────────────────
function computeQoLScores(data, city, cityId) {
  const cityZones = ZONES.filter(z => z.cityId === cityId);
  const counts = {};
  data.forEach(rec => {
    const lat = parseFloat(rec[city.qa311.latField]);
    const lng = parseFloat(rec[city.qa311.lngField]);
    if (!isFinite(lat) || !isFinite(lng)) return;
    const zone = findZoneForPoint(lat, lng, cityZones);
    if (zone) counts[zone.name] = (counts[zone.name] || 0) + 1;
  });
  const sorted = cityZones.map(z => counts[z.name] || 0).filter(c => c > 0).sort((a, b) => a - b);
  const median = sorted[Math.floor(sorted.length / 2)] || 1;
  const scoreMap = {};
  cityZones.forEach(zone => {
    const c = counts[zone.name] || 0;
    const raw = c === 0 ? 3.5 : 5 - 2.5 * Math.log10(Math.max(c, 1) / median);
    scoreMap[zone.name] = Math.round(Math.min(5, Math.max(1, raw)) * 10) / 10;
  });
  return scoreMap;
}

function computeAmenityScores(points, cityId) {
  const cityZones = ZONES.filter(z => z.cityId === cityId);
  const counts = {};
  points.forEach(({ lat, lng }) => {
    const zone = findZoneForPoint(lat, lng, cityZones);
    if (zone) counts[zone.name] = (counts[zone.name] || 0) + 1;
  });
  const densities = {};
  cityZones.forEach(zone => {
    const b = polyLayers[`${zone.cityId}:${zone.name}`]?.getBounds();
    const area = b ? (b.getNorth() - b.getSouth()) * (b.getEast() - b.getWest()) : 0.001;
    densities[zone.name] = (counts[zone.name] || 0) / Math.max(area, 1e-5);
  });
  const maxD = Math.max(...Object.values(densities), 1);
  const scoreMap = {};
  cityZones.forEach(zone => {
    const d = densities[zone.name] || 0;
    const raw = d === 0 ? 1 : 1 + (Math.log10(d + 1) / Math.log10(maxD + 1)) * 4;
    scoreMap[zone.name] = Math.round(Math.min(5, Math.max(1, raw)) * 10) / 10;
  });
  return scoreMap;
}

// ── Composite scoring ─────────────────────────────────────────────────────────
function activeCrimeScore(zone) {
  if (timeMode === 'day'   && zone.crimeScoreDay   != null) return zone.crimeScoreDay;
  if (timeMode === 'night' && zone.crimeScoreNight != null) return zone.crimeScoreNight;
  return zone.crimeScore;
}

function activeCrimeCount(zone) {
  if (timeMode === 'day'   && zone.crimeCountDay   != null) return zone.crimeCountDay;
  if (timeMode === 'night' && zone.crimeCountNight != null) return zone.crimeCountNight;
  return zone.crimeCount;
}

function computeComposite(zone) {
  const cs = activeCrimeScore(zone);
  if (cs === null) return zone.curatedScore;
  const parts = [{ v: cs, w: 0.60 }];
  if (zone.qolScore     !== null) parts.push({ v: zone.qolScore,     w: 0.25 });
  if (zone.amenityScore !== null) parts.push({ v: zone.amenityScore, w: 0.15 });
  const totalW = parts.reduce((s, x) => s + x.w, 0);
  const score  = parts.reduce((s, x) => s + x.v * (x.w / totalW), 0);
  return Math.round(Math.min(5, Math.max(1, score)) * 10) / 10;
}
