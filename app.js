// ── Apply live scores + trend ─────────────────────────────────────────────────
function applyScores(scoreMap, priorMap, cityId, dayMap, nightMap) {
  ZONES.filter(z => z.cityId === cityId).forEach(zone => {
    const s = scoreMap[zone.name];
    if (!s) return;
    zone.crimeScore = s.score;
    zone.crimeCount = s.count;
    if (dayMap?.[zone.name])   { zone.crimeScoreDay   = dayMap[zone.name].score;   zone.crimeCountDay   = dayMap[zone.name].count; }
    if (nightMap?.[zone.name]) { zone.crimeScoreNight = nightMap[zone.name].score; zone.crimeCountNight = nightMap[zone.name].count; }
    if (priorMap) {
      const p = priorMap[zone.name];
      if (p && p.weighted > 0 && s.weighted > 0) {
        const pct = (s.weighted - p.weighted) / p.weighted;
        zone.trend = pct > 0.18 ? 'worsening' : pct < -0.18 ? 'improving' : 'stable';
      }
    }
    applyComposite(zone);
  });
  if (activeZone && activeZone.cityId === cityId) showZone(activeZone);
}

