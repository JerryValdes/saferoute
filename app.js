if ('serviceWorker' in navigator) {
  navigator.serviceWorker.register('/saferoute/sw.js', { scope: '/saferoute/' })
    .catch(() => {}); // fail silently — offline still works if SW was previously installed
}

const offlineBanner = document.getElementById('offline-banner');
function updateOnlineStatus() {
  offlineBanner.classList.toggle('show', !navigator.onLine);
}
window.addEventListener('online',  updateOnlineStatus);
window.addEventListener('offline', updateOnlineStatus);
updateOnlineStatus();

// ── Initialize route mode button state ───────────────────────────────────────
(function () {
  const saved = localStorage.getItem('saferoute_routepref') || 'fastest';
  document.querySelectorAll('#route-mode-toggle .tt-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.rmode === saved);
  });
  const goBtn = document.getElementById('route-go-btn');
  if (goBtn && saved === 'safest') goBtn.textContent = 'Find safest route →';
})();

// ── Initialize landmark toggle state ─────────────────────────────────────────
(function () {
  const cb = document.getElementById('landmarks-toggle');
  if (cb) cb.checked = landmarksVisible;
})();

// ── Initialize traveler toggle state from localStorage ────────────────────────
(function () {
  const saved = localStorage.getItem('saferoute_traveler') || 'solo-adult';
  document.querySelectorAll('#traveler-toggle .tt-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.ttype === saved);
  });
})();

// ── First-load disclaimer ─────────────────────────────────────────────────────
function dismissDisclaimer() {
  localStorage.setItem('saferoute_disc_v1', '1');
  document.getElementById('disclaimer-overlay').classList.remove('open');
}
if (!localStorage.getItem('saferoute_disc_v1')) {
  document.getElementById('disclaimer-overlay').classList.add('open');
}
