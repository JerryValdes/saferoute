let timeMode = 'all'; // 'all' | 'day' | 'night'

function getCrimeHour(crime, dateField) {
  const v = crime[dateField];
  if (!v) return -1;
  const d = new Date(v);
  return isNaN(d) ? -1 : d.getHours();
}

function computeScores(crimes, city, cityId, hourFilter) {
  const cityZones = ZONES.filter(z => z.cityId === cityId);
  const weighted = {}, rawCount = {};
  cityZones.forEach(z => { weighted[z.name] = 0; rawCount[z.name] = 0; });

  const latF  = city.latField  || 'latitude';
  const lngF  = city.lngField  || 'longitude';
  const bboxF = city.bboxField;

  const filteredCrimes = hourFilter
    ? crimes.filter(c => { const h = getCrimeHour(c, city.dateField); return h >= 0 && hourFilter(h); })
    : crimes;

  filteredCrimes.forEach(c => {
    let lat, lng;
    if (bboxF && c[bboxF]?.coordinates?.length === 2) {
      [lng, lat] = c[bboxF].coordinates;                    // GeoJSON Point field
    } else if (bboxF && c[bboxF]?.latitude != null) {
      lat = parseFloat(c[bboxF].latitude);                  // Socrata location object
      lng = parseFloat(c[bboxF].longitude);
    } else if (c[latF] && c[lngF]) {
      lat = parseFloat(c[latF]);
      lng = parseFloat(c[lngF]);
    } else if (c.latitude && c.longitude) {
      lat = parseFloat(c.latitude);
      lng = parseFloat(c.longitude);
    }
    if (!lat || !lng || isNaN(lat) || isNaN(lng)) return;

    const w = crimeWeight(getCrimeType(c, city.typeFields));
    if (w === 0) return;

    const zone = findZoneForPoint(lat, lng, cityZones);
    if (zone) {
      weighted[zone.name] += w;
      rawCount[zone.name] += 1;
    }
  });

  // Ensure area is cached on every zone
  cityZones.forEach(z => { if (!z._area) z._area = estimateZoneAreaKm2(z.geometry); });

  // Median area of zones that received any crimes (avoid biasing on empty zones)
  const activeAreas = cityZones
    .filter(z => weighted[z.name] > 0)
    .map(z => z._area);
  if (activeAreas.length === 0) cityZones.forEach(z => activeAreas.push(z._area));
  activeAreas.sort((a, b) => a - b);
  // medianArea = area of a "typical" zone in this city → factor of 1 at median, no distortion
  const medianArea = activeAreas[Math.floor(activeAreas.length / 2)] || 1;

  const BASELINE = city.baseline, K = 2.5;
  const result = {};
  cityZones.forEach(z => {
    // Normalize: scale crimes as if this zone were median-area-sized.
    // Small zones (denser crime) score lower; large zones (sparser crime) score higher.
    const areaFactor = medianArea / z._area;
    const w = Math.max(weighted[z.name] * areaFactor, 1);
    const score = Math.max(1, Math.min(5, 5 - K * Math.log10(w / BASELINE)));
    result[z.name] = { score: Math.round(score * 10) / 10, count: rawCount[z.name], level: scoreToLevel(score), weighted: weighted[z.name] };
  });
  return result;
}

